// Crie um array com o nome de 5 jogos. Esse array não pode ter nomes
// repetidos. Utilizando o método foreach, percorra o array e mostre os
// valores na tela.


let jogos = ['Minecraft','Roblox','Fortnite','League of Legends','Rocket League']

jogos.forEach((jogos) => {
    console.log(`jogos: ${jogos}`);
    console.log(`---------------------------`);
})